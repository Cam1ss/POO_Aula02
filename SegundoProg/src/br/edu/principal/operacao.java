package br.edu.principal;

public class operacao {

	public static void main(String[] args) {
	int f = 21;
	int p = 12;
	int m = 23;
    System.out.println("Valor das variaveis...");
    System.out.println(" f = " + f);
    System.out.println(" p = " + p);
    System.out.println(" m = " + m);
    System.out.println("Maior que...");
    System.out.println(" f > " + (f > p));
    System.out.println(" p > " + (p > m));
    System.out.println(" m > " + (m > f));
    System.out.println("Maior ou igual...");
    System.out.println(" f >= " + (f >= p));
    System.out.println(" p >= " + (p >= m));
    System.out.println(" m >= " + (m >= f));
    System.out.println("Menor que...");
    System.out.println(" f < " + (f < p));
    System.out.println(" p < " + (p < m));
    System.out.println(" m < " + (m < f));
    System.out.println("Maior ou igual...");
    System.out.println(" f <= " + (f <= p));
    System.out.println(" p <= " + (p <= m));
    System.out.println(" m <= " + (m <= f));
    System.out.println("Igual...");
    System.out.println(" f == p = " + (f == p));
    System.out.println(" m == p = " + (m == p));
    System.out.println("Diferente...");
    System.out.println(" f != p = " + (f != p));
    System.out.println(" m != p = " + (m != p));
    
	}
}
